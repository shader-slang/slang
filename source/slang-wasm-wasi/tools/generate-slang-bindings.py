def main():
    repo_root = os.path.dirname(
        os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
    )

    parser = argparse.ArgumentParser(description="Generate Slang enum bindings.")
    parser.add_argument(
        "--slang-h",
        default=os.path.join(repo_root, "include", "slang.h"),
        help="Path to include/slang.h",
    )
    parser.add_argument(
        "--cpp-out",
        default=os.path.join(
            repo_root,
            "source", "slang-wasm-wasi", "slang-wasm-wasi-enum-generated.cpp",
        ),
        help="Output path for the C++ metadata translation unit",
    )
    parser.add_argument(
        "--java-out",
        default=None,
        help=(
            "Output directory for generated Java enum source files. "
            "Omit to skip Java generation (the default)."
        ),
    )
    parser.add_argument(
        "--java-package",
        default="org.shaderslang.wasm.enums",
        help="Java package declared in the generated enum source files",
    )
    args = parser.parse_args()

    with open(args.slang_h, encoding="utf-8") as f:
        content = f.read()

    # Collect per-enum (java_name → [(java_member_name, int_value)]) data.
    enum_data: dict = {}
    enum_comments: dict = {}  # java_name → {java_member_name: comment_text}

    for spec in ENUM_CONFIG:
        # Extract raw (C_member_name, int_value) pairs.
        if spec.prefix:
            # Anonymous or flag enum: locate members by their name prefix.
            raw = extract_prefixed_members(content, spec.prefix)
            raw_comments = extract_prefixed_member_comments(content, spec.prefix)
        else:
            # Named enum (covers both "enum class" and "enum Name : Base").
            raw = extract_named_enum(content, spec.c_name)
            raw_body = _find_raw_enum_body(content, spec.c_name)
            raw_comments = extract_member_comments(raw_body) if raw_body else {}

        if raw is None:
            print(
                f"WARNING: enum {spec.c_name!r} not found in {args.slang_h}",
                file=sys.stderr,
            )
            continue
        if not raw:
            print(
                f"WARNING: enum {spec.c_name!r} has no parseable members",
                file=sys.stderr,
            )
            continue

        c_names = [n for n, _ in raw]
        values = [v for _, v in raw]
        _, stripped = strip_prefix(c_names, spec.prefix)
        enum_data[spec.java_name] = list(zip(stripped, values))

        # Map stripped Java names → comments, skipping members with no comment.
        java_comments: dict = {}
        for c_name, java_name in zip(c_names, stripped):
            if c_name in raw_comments:
                java_comments[java_name] = raw_comments[c_name]
        enum_comments[spec.java_name] = java_comments

    # Write C++ output.
    cpp_dir = os.path.dirname(args.cpp_out)
    if cpp_dir:
        os.makedirs(cpp_dir, exist_ok=True)
    with open(args.cpp_out, "w", encoding="utf-8") as f:
        f.write(generate_cpp(enum_data))
    print(f"Wrote {args.cpp_out}")

    # Write Java outputs only when explicitly requested via --java-out.
    if args.java_out:
        os.makedirs(args.java_out, exist_ok=True)
        for spec in ENUM_CONFIG:
            if spec.java_name not in enum_data:
                continue
            java_source = generate_java_enum(
                spec,
                enum_data[spec.java_name],
                enum_comments.get(spec.java_name, {}),
                args.java_package,
            )
            out_path = os.path.join(args.java_out, f"{spec.java_name}.java")
            with open(out_path, "w", encoding="utf-8") as f:
                f.write(java_source)
            print(f"Wrote {out_path}")


if __name__ == "__main__":
    main()
