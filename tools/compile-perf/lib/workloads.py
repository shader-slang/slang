# Suspected-regression features
# --------------------------------------------------------------------------- #

# Call depth within a group. Work scales by *breadth* (number of groups), while
# nesting stays bounded so we don't hit the compiler's type-nesting recursion cap
# (~32 levels in practice). 8 gives ~4× margin and is empirically safe up to
# n=1280 (the largest sweep size used).
_GROUP_DEPTH = 8


def gen_autodiff(n):
    """n differentiable functions arranged in bounded-depth groups, each group
    differentiated in both forward and reverse mode, plus a differentiable
    generic. Stresses the autodiff IR transform (inside linkAndOptimizeIR) and
    the front-end checking of [Differentiable]. Scales by breadth.

    Scaling null: n scales group breadth with depth bounded by _GROUP_DEPTH, so ideal autodiff cost is O(n).
    """
    s = [_HEADER, _buf()]
    s.append("[Differentiable]\n")
    s.append("float leaf(float x) { return x * x + sin(x) * cos(x); }\n\n")
    group_tops = []
    for i in range(n):
        prev = "leaf" if (i % _GROUP_DEPTH == 0) else f"chain_{i - 1}"
        s.append("[Differentiable]\n")
        s.append(
            f"float chain_{i}(float x) {{ return {prev}(x) * 1.0009 + sin(x) * 0.5 - cos(x) * 0.25; }}\n"
        )
        if (i % _GROUP_DEPTH == _GROUP_DEPTH - 1) or i == n - 1:
            group_tops.append(i)
    s.append("\n")
    # A differentiable generic instantiated for float.
    s.append("[Differentiable]\n")
    s.append("T poly<T : IDifferentiable & IFloat>(T x) { return x * x * x + x; }\n\n")
    s.append('[shader("compute")]\n[numthreads(1,1,1)]\n')
    s.append("void computeMain(uint3 tid : SV_DispatchThreadID)\n{\n")
    s.append("    float x = outBuf[tid.x];\n    float acc = 0.0;\n")
    for top in group_tops:
        s.append("    { var dp = diffPair(x, 1.0);\n")
        s.append(f"      bwd_diff(chain_{top})(dp, 1.0); acc += dp.d;\n")
        s.append(f"      acc += fwd_diff(chain_{top})(diffPair(x, 1.0)).d; }}\n")
    s.append("    acc += fwd_diff(poly<float>)(diffPair(x, 1.0)).d;\n")
    s.append("    outBuf[0] = acc;\n}\n")
    return {"autodiff.slang": "".join(s)}


def gen_dynamic_dispatch(n):
    """One interface, n implementations, called through an existential whose
    concrete type is chosen at runtime (defeats static specialization, forcing
    witness-table dynamic dispatch). Stresses dispatch lowering / specializeModule.

    Scaling null: n scales implementations and switch cases; generated code is O(n), so ideal cost is O(n).
    """
    s = [_HEADER, _buf()]
    s.append("[anyValueSize(16)]\ninterface IShape { float eval(float x); }\n\n")
    for i in range(n):
        s.append(
            f"struct Shape{i} : IShape {{ float eval(float x) {{ return x * {i + 1}.0 + {i}.0 + sin(x * {i + 1}.0); }} }}\n"
        )
    s.append("\nfloat dispatch(int id, float x)\n{\n    IShape s;\n    switch (id)\n    {\n")
    for i in range(n):
        s.append(f"    case {i}: s = Shape{i}(); break;\n")
    s.append("    default: s = Shape0(); break;\n    }\n    return s.eval(x);\n}\n\n")
    s.append("float run(IShape s, float x) { return s.eval(x); }\n\n")
    s.append('[shader("compute")]\n[numthreads(1,1,1)]\n')
    s.append("void computeMain(uint3 tid : SV_DispatchThreadID)\n{\n")
    s.append("    float acc = 0.0;\n")
    s.append("    int base = int(tid.x);\n")
    s.append(f"    for (int i = 0; i < {n}; ++i)\n")
    s.append("        acc += dispatch((base + i) % " + str(n) + ", outBuf[0] + float(i));\n")
    s.append("    outBuf[0] = acc;\n}\n")
    return {"dynamic_dispatch.slang": "".join(s)}


def gen_diagnostics_clean(n):
    """N functions with distinct compile-time constants, no errors.

    Scaling null: n scales independent declarations; ideal cost is O(n).
    """
    s = [_HEADER, _buf()]
    for i in range(n):
        s.append(f"static const float k_{i} = {i % 7}.0;\n")
        s.append(f"float ok_{i}(float x) {{ return x + k_{i} * {i % 5 + 1}.0; }}\n")
    s.append('\n[shader("compute")]\n[numthreads(1,1,1)]\n')
    s.append("void computeMain()\n{\n    float acc = 0.0;\n")
    s.append(f"    for (int i = 0; i < {n}; ++i) acc += ok_0(float(i));\n")
    s.append("    outBuf[0] = acc;\n}\n")
    return {"diagnostics_clean.slang": "".join(s)}


def gen_existential_aggregate(n):
    """An interface-typed *field* inside a struct (`Scene { IMat m; ... }`), with
    n implementations selected at runtime through a switch. Unlike
    `dynamic_dispatch` (a bare local existential), boxing the existential in an
    aggregate forces `legalizeExistentialTypeLayout` to float the existential
    field out and recompute the parent layout, and feeds specializeModule a
    witness-table-per-case blowup. Stresses specializeModule +
    legalizeExistentialTypeLayout + the downstream simplifyIR. Scales by breadth
    (number of implementations / switch cases).

    Scaling null: n scales cases, each an O(1) body; ideal specialization cost is O(n).
    """
    s = [_HEADER, _buf()]
    s.append("[anyValueSize(16)]\ninterface IMat { float shade(float x); }\n\n")
    for i in range(n):
        s.append(
            f"struct M{i} : IMat {{ float shade(float x) {{ return x * {i + 1}.0 + {i}.0 + sin(x * {i % 7 + 1}.0); }} }}\n"
        )
    s.append("\nstruct Scene { IMat m; float w; }\n\n")
    s.append("float shadeScene(Scene sc, float x) { return sc.m.shade(x) * sc.w; }\n\n")
    s.append('[shader("compute")]\n[numthreads(64,1,1)]\n')
    s.append("void computeMain(uint3 tid : SV_DispatchThreadID)\n{\n")
    s.append("    float acc = 0.0;\n    int base = int(tid.x);\n")
    s.append(f"    for (int i = 0; i < {n}; ++i)\n    {{\n        Scene sc;\n")
    s.append(f"        switch ((base + i) % {n})\n        {{\n")
    for i in range(n):
        s.append(f"        case {i}: sc.m = M{i}(); break;\n")
    s.append("        default: sc.m = M0(); break;\n        }\n")
    s.append("        sc.w = float(i);\n        acc += shadeScene(sc, outBuf[0] + float(i));\n    }\n")
    s.append("    outBuf[0] = acc;\n}\n")
    return {"existential_aggregate.slang": "".join(s)}


