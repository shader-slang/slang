def gen_parse(n):
    """Large but semantically trivial source: many functions, each a long
    arithmetic expression. Stresses lex/parse with cheap sema.

    Scaling null: n scales statement count; ideal parse cost is O(n).
    """
    s = [_HEADER, _buf()]
    for i in range(n):
        terms = " ".join(
            f"+ {j % 9}.0 * x" if j % 2 else f"- {j % 7}.0 * x" for j in range(40)
        )
        s.append(f"float p_{i}(float x) {{ return x {terms}; }}\n")
    s.append('\n[shader("compute")]\n[numthreads(1,1,1)]\n')
    s.append("void computeMain() { outBuf[0] = p_0(outBuf[0]); }\n")
    return {"parse.slang": "".join(s)}


def gen_sema_generics(n):
    """n distinct generic functions with interface constraints, each instantiated
    at several concrete types. Stresses semantic checking, generic constraint
    satisfaction and instantiation in the front-end.

    Scaling null: n scales instantiation count against a fixed generic set; ideal checking cost is O(n).
    """
    s = [_HEADER, _buf()]
    for i in range(n):
        s.append(
            f"T poly_{i}<T : IArithmetic>(T a, T b) {{ return a * a + b * a + a + b; }}\n"
        )
    s.append('\n[shader("compute")]\n[numthreads(1,1,1)]\n')
    s.append("void computeMain()\n{\n")
    s.append("    float facc = 0.0; int iacc = 0; uint uacc = 0u;\n")
    s.append("    float fx = outBuf[0]; int ix = int(outBuf[0]); uint ux = uint(outBuf[0]);\n")
    for i in range(n):
        s.append(f"    facc += poly_{i}<float>(fx, fx + 1.0);\n")
        s.append(f"    iacc += poly_{i}<int>(ix, ix + 1);\n")
        s.append(f"    uacc += poly_{i}<uint>(ux, ux + 1u);\n")
    s.append("    outBuf[0] = facc + float(iacc) + float(uacc);\n}\n")
    return {"sema_generics.slang": "".join(s)}


def gen_generic_nesting(n):
    """One generic struct instantiated with itself n levels deep: a chain of
    `typealias T_i = Pair<Leaf, T_{i-1}>` where `Pair` is constrained to an
    interface that extends another. Every other generic workload in the suite
    scales BREADTH (many shallow instantiations); this one scales DEPTH, the
    axis on which the front-end's substitution / inheritance-witness work is
    known to multiply per level. The shape arises in real code that composes
    generic "model"/"module" types recursively (e.g. chained ML evaluator
    stacks), where each wrapper layer adds one level of nesting.

    Scaling null: each level adds O(1) declarations, so ideal front-end cost
    is O(n). Measured behavior when this was added (2026-07): substitution
    work grows ~3-4x PER LEVEL (exponential), with the wall-clock knee near
    depth 18 — v2025.14 through tip-of-tree all take ~17-20 s at depth 24.
    The sweep ladder deliberately stays at or below the knee; the finding to
    watch is the top rung's multiple of the linear expectation moving.
    """
    s = [_HEADER]
    s.append("interface IBase {}\n")
    s.append("interface IModel : IBase {}\n")
    s.append("struct Leaf : IModel {}\n\n")
    s.append("struct Pair<First : IModel, Second : IModel> : IModel\n")
    s.append("{\n    First first;\n    Second second;\n}\n\n")
    s.append("typealias T0 = Leaf;\n")
    for i in range(1, n + 1):
        s.append(f"typealias T{i} = Pair<Leaf, T{i-1}>;\n")
    s.append(f"\nT{n} value;\n\n")
    s.append('[shader("compute")]\n[numthreads(1, 1, 1)]\n')
    s.append("void computeMain() {}\n")
    return {"generic_nesting.slang": "".join(s)}


def gen_generic_nesting_eval(n):
    """The generic_nesting chain with a method REQUIRED by the constraint
    interface and called recursively through the witnesses (`first.eval() +
    second.eval()`), so overload resolution and inheritance-witness lookup run
    at every nesting level instead of only type checking the aliases. Same
    depth axis as generic_nesting, much steeper constant: measured ~2.9x per
    level when added (2026-07), vs ~3-4x per level from substitution alone
    but starting an order of magnitude higher at equal depth.

    Scaling null: each level adds O(1) declarations and one call site, so
    ideal front-end cost is O(n). The ladder tops out below generic_nesting's
    because the per-level multiplier bites sooner.
    """
    s = [_HEADER]
    s.append("interface IBase {}\n")
    s.append("interface IModel : IBase { float eval(); }\n")
    s.append("struct Leaf : IModel { float eval() { return 1.0; } }\n\n")
    s.append("struct Pair<First : IModel, Second : IModel> : IModel\n")
    s.append("{\n    First first;\n    Second second;\n")
    s.append("    float eval() { return first.eval() + second.eval(); }\n}\n\n")
    s.append("typealias T0 = Leaf;\n")
    for i in range(1, n + 1):
        s.append(f"typealias T{i} = Pair<Leaf, T{i-1}>;\n")
    s.append("\nRWStructuredBuffer<float> outBuf;\n\n")
    s.append('[shader("compute")]\n[numthreads(1, 1, 1)]\n')
    s.append(f"void computeMain() {{ T{n} v; outBuf[0] = v.eval(); }}\n")
    return {"generic_nesting_eval.slang": "".join(s)}


def gen_interface_depth(n):
    """A linear interface inheritance chain n deep (`interface I_k : I_{k-1}`)
    with one struct conforming at the bottom and one generic function
    constrained to the TOP, so conformance checking walks the whole chain.
    Scales the interface-hierarchy depth axis of calcInheritanceInfo — a
    different graph than generic_nesting's type-argument nesting.

    Scaling null: each level adds O(1) declarations, so ideal inheritance-
    info cost is O(n) (or O(n^2) for an all-pairs facet walk); measured
    ~N^3.4 when added (2026-07).
    """
    s = [_HEADER]
    s.append("interface I0 { float get(); }\n")
    for i in range(1, n + 1):
        s.append(f"interface I{i} : I{i-1} {{ }}\n")
    s.append(f"\nstruct S : I{n} {{ float get() {{ return 1.0; }} }}\n\n")
    s.append("float use<T : I0>(T t) { return t.get(); }\n\n")
    s.append("RWStructuredBuffer<float> outBuf;\n\n")
    s.append('[shader("compute")]\n[numthreads(1, 1, 1)]\n')
    s.append("void computeMain() { S s; outBuf[0] = use(s); }\n")
    return {"interface_depth.slang": "".join(s)}


# --------------------------------------------------------------------------- #
# Type checking: operator overload resolution + implicit conversions
# --------------------------------------------------------------------------- #
# These isolate the front-end type-checker's most quietly expensive work: every
# binary operator and every cross-type assignment runs overload resolution over
# a candidate set and ranks implicit-conversion costs. `parse` deliberately uses
# uniform-`float` arithmetic so each operator matches one overload trivially;
# the workloads below force a *mixed-type* decision at every node instead.

def gen_operator_typecheck(n):
    """`n` functions, each a long arithmetic expression whose operands alternate
    among float/int/uint (typed locals and literals of differing kinds). Every
    `+`/`-`/`*` must resolve the builtin arithmetic operator overload set and
    insert an implicit conversion to the common type — the "checking the types of
    1 and 2 in 1 + 2" cost, multiplied across many nodes. Contrast `parse`, whose
    same-type float expressions skip overload ranking. Module mode -> the signal
    is SemanticChecking, not codegen.

    Scaling null: n scales fixed-length expressions; ideal typecheck cost is O(n).
    """
    s = [_HEADER, _buf()]
    # Each term mixes operand types so the common-type/coercion decision is
    # non-trivial; all conversions are standard widening (int/uint -> float) so
    # the source compiles cleanly across the whole release window.
    terms = [
        "+ 2 * fx", "- ix + 3u", "+ 4.0 * ix", "- ux * 5", "+ fx * 6u",
        "- 7 + ux", "+ ix * 8.0", "- 9u + fx", "+ ux * 10", "- fx * 11",
    ]
    for i in range(n):
        body = " ".join(terms[(i + j) % len(terms)] for j in range(24))
        s.append(
            f"float op_{i}(float fx, int ix, uint ux) {{ return fx {body}; }}\n"
        )
    s.append('\n[shader("compute")]\n[numthreads(1,1,1)]\n')
    s.append("void computeMain()\n{\n    float acc = 0.0;\n")
    s.append("    float fx = outBuf[0]; int ix = int(outBuf[0]); uint ux = uint(outBuf[0]);\n")
    # Cap computeMain call sites at 64: beyond this the entry point itself (not
    # the per-function checking cost we're measuring) becomes the bottleneck.
    for i in range(min(n, 64)):
        s.append(f"    acc += op_{i}(fx, ix, ux);\n")
    s.append("    outBuf[0] = acc;\n}\n")
    return {"operator_typecheck.slang": "".join(s)}


def gen_implicit_conversion(n):
    """`n` functions, each a cascade of cross-type conversions: scalar widening
    (int/uint -> float), splats, mixed initializer lists (per-element coercion),
    vector compose/truncate, and explicit narrowing. Stresses the coercion /
    conversion-cost engine in semantic checking rather than operator overloading.

    Scaling null: n scales conversion sites against a FIXED type set; ideal cost is O(n).
    """
    s = [_HEADER, _buf()]
    for i in range(n):
        b = [f"float conv_{i}()", "{"]
        b.append(f"    int i = {i % 7}; uint u = {i % 5}u; float f = {i % 9}.0;")
        b.append("    float a = i;")                      # int -> float (widen)
        b.append("    float b = u;")                      # uint -> float (widen)
        b.append("    float2 v2 = f;")                    # scalar -> vector splat
        b.append("    float3 v3 = float3(i, u, f);")      # mixed init: per-elem coerce
        b.append("    float4 v4 = float4(v3, a);")        # vector compose
        b.append("    int2 iv = int2(v2);")               # float2 -> int2 (explicit)
        b.append("    float3 w = v4.xyz;")                # swizzle / truncate
        b.append("    return a + b + v2.x + v3.y + v4.w + float(iv.x) + w.z;")
        b.append("}")
        s.append("\n".join(b) + "\n")
    s.append('\n[shader("compute")]\n[numthreads(1,1,1)]\n')
    s.append("void computeMain()\n{\n    float acc = 0.0;\n")
    # Cap computeMain call sites at 64: beyond this the entry point itself (not
    # the per-function checking cost we're measuring) becomes the bottleneck.
    for i in range(min(n, 64)):
        s.append(f"    acc += conv_{i}();\n")
    s.append("    outBuf[0] = acc;\n}\n")
    return {"implicit_conversion.slang": "".join(s)}


def gen_overload_resolution(n):
    """A large user-defined overload set (`pick`/`pick2`, scalar + vector + two-arg
    candidates), then `n` call sites with rotating argument types. Every call
    enumerates all candidates and ranks them by conversion cost to choose the best
    match — isolating the candidate-ranking cost that scales with overload-set
    size times call-site count.

    Scaling null: n scales call sites against a FIXED candidate set; ideal resolution cost is O(n).
    """
    s = [_HEADER, _buf()]
    for ty in ("float", "int", "uint", "float2", "float3", "float4"):
        s.append(f"float pick({ty} x) {{ return float(x[0] + x[0]); }}\n"
                 if ty.startswith("float") and ty != "float"
                 else f"float pick({ty} x) {{ return float(x) + 1.0; }}\n")
    # two-argument overloads exercise multi-parameter rank comparison
    for a, bb in (("float", "float"), ("int", "float"), ("float", "int"),
                  ("int", "int"), ("uint", "float"), ("float", "uint")):
        s.append(f"float pick2({a} a, {bb} b) {{ return float(a) + float(b); }}\n")
    s.append("\n")
    args1 = ["1.0", "2", "3u", "float2(1.0)", "float3(1.0)", "float4(1.0)"]
    args2 = [("1.0", "2.0"), ("3", "4.0"), ("5.0", "6"), ("7", "8"),
             ("9u", "10.0"), ("11.0", "12u")]
    for i in range(n):
        a = args1[i % len(args1)]
        p, q = args2[i % len(args2)]
        s.append(f"float call_{i}() {{ return pick({a}) + pick2({p}, {q}); }}\n")
    s.append('\n[shader("compute")]\n[numthreads(1,1,1)]\n')
    s.append("void computeMain()\n{\n    float acc = 0.0;\n")
    # Cap computeMain call sites at 64: beyond this the entry point itself (not
    # the per-function checking cost we're measuring) becomes the bottleneck.
    for i in range(min(n, 64)):
        s.append(f"    acc += call_{i}();\n")
    s.append("    outBuf[0] = acc;\n}\n")
    return {"overload_resolution.slang": "".join(s)}


def gen_specialization(n):
    """A generic struct over a type parameter, instantiated at n distinct
    wrapper types. Forces specializeModule to clone the generic n ways.

    Scaling null: n scales instantiations of O(1) bodies; ideal specialization cost is O(n).
    """
    s = [_HEADER, _buf()]
    s.append("interface IVal { float get(); }\n\n")
    for i in range(n):
        s.append(f"struct V{i} : IVal {{ float get() {{ return {i}.0 + 1.0; }} }}\n")
    s.append("\nstruct Box<T : IVal> { T v; float compute() { return v.get() * 2.0 + v.get(); } }\n\n")
    s.append('[shader("compute")]\n[numthreads(1,1,1)]\n')
    s.append("void computeMain()\n{\n    float acc = 0.0;\n")
    for i in range(n):
        s.append(f"    {{ Box<V{i}> b; acc += b.compute(); }}\n")
    s.append("    outBuf[0] = acc;\n}\n")
    return {"specialization.slang": "".join(s)}


def gen_inlining(n):
    """n [ForceInline] functions in bounded-depth groups. The inliner collapses
    each group into its group-top; the entry sums all group-tops, giving the SSA
    simplifier large bodies to chew on. Scales by breadth (bounded nesting).

    Scaling null: n scales call sites with bounded inlining depth; ideal cost is O(n).
    """
    s = [_HEADER, _buf()]
    group_tops = []
    for i in range(n):
        if i % _GROUP_DEPTH == 0:
            s.append(f"[ForceInline] float step_{i}(float x) {{ return x * 1.01 + sin(x); }}\n")
        else:
            s.append(
                f"[ForceInline] float step_{i}(float x) {{ float t = step_{i - 1}(x); return t * 1.01 + cos(t) - sin(x * 0.5); }}\n"
            )
        if (i % _GROUP_DEPTH == _GROUP_DEPTH - 1) or i == n - 1:
            group_tops.append(i)
    s.append('\n[shader("compute")]\n[numthreads(1,1,1)]\n')
    s.append("void computeMain()\n{\n    float x = outBuf[0]; float acc = 0.0;\n")
    for top in group_tops:
        s.append(f"    acc += step_{top}(x);\n")
    s.append("    outBuf[0] = acc;\n}\n")
    return {"inlining.slang": "".join(s)}


def gen_codegen(n):
    """A single moderately complex shader. Compiled to multiple targets by the
    harness (same source, varying -target) to isolate generateOutput cost.
    ``n`` scales the amount of straight-line math the backend must emit.

    Scaling null: n scales op count and the emitted output is O(n); ideal codegen cost is O(n).
    """
    s = [_HEADER, _buf()]
    s.append('[shader("compute")]\n[numthreads(64,1,1)]\n')
    s.append("void computeMain(uint3 tid : SV_DispatchThreadID)\n{\n")
    s.append("    float x = outBuf[tid.x];\n    float acc = x;\n")
    for i in range(n):
        s.append(
            f"    acc = acc * 1.0009 + sin(acc + {i}.0) * 0.5 - cos(acc * 0.5) * 0.25 + sqrt(abs(acc) + {i + 1}.0);\n"
        )
    s.append("    outBuf[tid.x] = acc;\n}\n")
    return {"codegen.slang": "".join(s)}


def gen_module_link(n):
    """n importable modules plus a main that imports and uses all of them. The
    harness precompiles each module to .slang-module, then compiles main against
    them, stressing module read + linkIR.

    Scaling null: n scales module count, each O(1); ideal load+link cost is O(n).
    """
    files = {}
    for i in range(n):
        files[f"mod_{i}.slang"] = (
            _HEADER
            + f"module mod_{i};\n\n"
            + f"public float mfun_{i}(float x) {{ return x * {i + 1}.0009 + sin(x + {i}.0); }}\n"
        )
    main = [_HEADER]
    for i in range(n):
        main.append(f"import mod_{i};\n")
    main.append("\n" + _buf())
    main.append('[shader("compute")]\n[numthreads(1,1,1)]\n')
    main.append("void computeMain()\n{\n    float acc = outBuf[0];\n")
    for i in range(n):
        main.append(f"    acc = mfun_{i}(acc);\n")
    main.append("    outBuf[0] = acc;\n}\n")
    files["link_main.slang"] = "".join(main)
    return files


