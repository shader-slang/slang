"""Deterministic, size-scalable Slang workload generators for the compile-time
perf suite.

Each generator is a function ``gen(n) -> dict[str, str]`` mapping a filename to
Slang source. Most workloads are a single file; the module-link workload emits
several. Generators must be deterministic (same ``n`` -> identical bytes) so a
release sweep compares like with like.

Conventions:
- Every compilable workload exposes a ``[shader("compute")]`` entry named
  ``computeMain`` so ``slangc`` auto-discovers it; results are written to a
  ``RWStructuredBuffer`` so nothing is dead-code-eliminated.
"""

import os

# ASCII only: generated sources must be byte-identical on every platform
# (Windows CI writes them with a legacy default codec unless told otherwise,
# and slangc reads bytes as UTF-8), so no typographic characters here.
_HEADER = "// AUTO-GENERATED by perf-suite/workloads.py - do not edit by hand.\n"
_CORPUS = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))), "corpus")

# "Scaling null:" convention — each swept generator's docstring ends with a
# one-line scaling null: the IDEAL asymptotic cost of the workload in its size
# knob n, i.e. the hypothesis the sweep tests against. A sweep exponent
# meaningfully above the null (see sweep_report's super-linearity flags) means
# the amplified pass scales worse than its input, which is the finding.
# complexity_ladder is marked "Scaling reference (not a null)" because it
# deliberately scales several dimensions together and has no single-pass ideal.


def _buf():
    """Standard output-buffer declaration used by every compilable workload.

    All workload generators write results into `outBuf` so that nothing is
    dead-code-eliminated before the targeted compiler stage runs.
    """
    return "RWStructuredBuffer<float> outBuf;\n\n"


# --------------------------------------------------------------------------- #
# Per-compile floor
# --------------------------------------------------------------------------- #

def gen_minimal(n):
    """Near-empty shader: isolates the fixed per-compile floor — core-module
    deserialization (readSerializedModuleIR/AST, loadBuiltinModule) plus linkIR
    of the user module against the core module — with negligible user-code work.

    This is the cleanest detector for "the standard library got heavier"
    regressions: when the core module grows, every compile pays here regardless
    of which language features it uses (the class of regression introduced by the
    auto-diff overhaul in PR #9808, where an empty shader's linkIR rose ~27x)."""
    s = [_HEADER, _buf()]
    s.append('[shader("compute")]\n[numthreads(1,1,1)]\n')
    s.append("void computeMain(uint3 tid : SV_DispatchThreadID) { outBuf[tid.x] = float(tid.x); }\n")
    return {"minimal.slang": "".join(s)}


def gen_ir_builder(n):
    """One enormous straight-line function: `n` trivial integer SSA ops, almost
    no semantic work. Stresses IR instruction construction / dedup / hash-cons
    and the SSA simplifier (the shared IRBuilder layer PR #9808 touched).

    Scaling null: n scales straight-line op count; ideal cost is O(n) with amortized-constant hash-consing, so super-linear sweep growth means IR dedup/use-list structures scale worse than the code.
    """
    s = [_HEADER, _buf()]
    s.append('[shader("compute")]\n[numthreads(1,1,1)]\n')
    s.append("void computeMain(uint3 tid : SV_DispatchThreadID)\n{\n")
    s.append("    int acc = int(tid.x);\n")
    for i in range(n):
        op = "*" if i % 3 == 0 else ("+" if i % 3 == 1 else "^")
        s.append(f"    acc = acc {op} {i % 13 + 1};\n")
    s.append("    outBuf[0] = float(acc);\n}\n")
    return {"ir_builder.slang": "".join(s)}


def gen_serialize(n):
    """A large module of `n` public functions, compiled to a .slang-module.
    Stresses IR/AST serialization (writeSerializedModule*) — the write side of
    the cost whose read side (readSerializedModule*) the `minimal` floor test
    and module loading exercise.

    Scaling null: n scales function count; ideal serialization cost is O(n).
    """
    s = [_HEADER]
    for i in range(n):
        s.append(
            f"public float sfn_{i}(float x, float y) {{ return x * {i % 7 + 1}.0 + y * {i % 5 + 1}.0 + sin(x + {i}.0); }}\n"
        )
    return {"serialize.slang": "".join(s)}


def gen_conformance(n):
    """`n` structs each conforming to a shared interface. Stresses interface
    conformance checking / witness synthesis in the front end (compiled to a
    .slang-module so the signal is sema, not codegen).

    Scaling null: n scales conforming structs against ONE interface; ideal checking cost is O(n).
    """
    s = [_HEADER]
    s.append("interface IFace { float f(float x); int g(); }\n\n")
    for i in range(n):
        s.append(
            f"struct C{i} : IFace {{ float f(float x) {{ return x * {i % 9 + 1}.0 + {i}.0; }} "
            f"int g() {{ return {i}; }} }}\n"
        )
    return {"conformance.slang": "".join(s)}


def gen_loop_unroll(n):
    """A force-unrolled loop of `n` iterations. Stresses loop unrolling
    (unrollLoopsInModule) and the downstream SSA simplify on the unrolled body.

    Scaling null: the unrolled output is O(n), so ideal cost is linear in n (linear in output size).
    """
    s = [_HEADER, _buf()]
    s.append('[shader("compute")]\n[numthreads(1,1,1)]\n')
    s.append("void computeMain(uint3 tid : SV_DispatchThreadID)\n{\n")
    s.append("    float acc = outBuf[tid.x];\n")
    s.append(f"    [ForceUnroll] for (int i = 0; i < {n}; ++i)\n")
    s.append("        acc = acc * 1.0009 + sin(acc + float(i)) * 0.5 - cos(acc * 0.5);\n")
    s.append("    outBuf[0] = acc;\n}\n")
    return {"loop_unroll.slang": "".join(s)}


